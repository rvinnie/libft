#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*a;

	a = s;
	i = 0;
	if (n == 0)
		return ;
	while (i < n)
	{
		a[i] = '\0';
		i++;
	}
	return ;
}
